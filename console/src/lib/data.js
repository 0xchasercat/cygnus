let reqSeq = 0;
export function nextRequest() {
  const p = PATHS[reqSeq++ % PATHS.length];
  const jitter = Math.random();
  const cold = jitter > 0.93;
  const dur = cold
    ? Math.round(38 + jitter * 40)
    : Math.round(3 + jitter * 26);
  const now = new Date();
  const hh = String(now.getHours()).padStart(2, '0');
  const mm = String(now.getMinutes()).padStart(2, '0');
  const ss = String(now.getSeconds()).padStart(2, '0');
  return {
    id: reqSeq,
    time: `${hh}:${mm}:${ss}`,
    method: p[0],
    path: p[1],
    app: p[2],
    status: jitter > 0.985 ? 500 : p[3],
    dur,
    cold,
  };
}

