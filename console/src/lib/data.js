// latency series for Observe — [p50, p99] pairs, ms
export const latency = Array.from({ length: 48 }, (_, i) => {
  const wave = Math.sin(i / 5.2) * 6 + Math.sin(i / 2.3) * 3;
  const p50 = 24 + wave + (i % 7 === 3 ? 4 : 0);
  const p99 = 58 + wave * 2.4 + (i % 11 === 5 ? 26 : 0) + (i % 17 === 9 ? 14 : 0);
  return [Math.round(p50 * 10) / 10, Math.round(p99 * 10) / 10];
});

