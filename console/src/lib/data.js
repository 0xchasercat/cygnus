export function identicon(name) {
  let h = 0;
  for (let i = 0; i < name.length; i++) h = (h * 31 + name.charCodeAt(i)) >>> 0;
  const [bg, fg] = DUO[h % DUO.length];
  const angle = 30 + (h % 5) * 60;
  return { bg, fg, angle };
}

